// ===== 純関数 =====
// '★★☆' → 2（★の数）。不明は 0
function covLevel(l){
  var n = (String(l || '').match(/★/g) || []).length;
  return (n >= 1 && n <= 4) ? n : 0;
}
// sub が最初に一致した単元の添字（無ければ -1）。「上にある単元が勝つ」
function covMatchUnit(units, sub){
  var s = String(sub || '');
  if (!s) return -1;
  for (var i = 0; i < units.length; i++){
    var ks = units[i].k;
    for (var j = 0; j < ks.length; j++){ if (s.indexOf(ks[j]) >= 0) return i; }
  }
  return -1;
}
// items:[{sub,level}] × 単元マスター → 集計
function covAnalyze(items, units){
  var res = units.map(function(u){ return { u: u.u, count: 0, byLevel: { 0:0, 1:0, 2:0, 3:0, 4:0 }, subs: {} }; });
  var unmatched = {};
  (items || []).forEach(function(it){
    if (!it || !it.sub) return;
    var i = covMatchUnit(units, it.sub);
    if (i < 0){ unmatched[it.sub] = (unmatched[it.sub] || 0) + 1; return; }
    var r = res[i];
    r.count++;
    r.byLevel[covLevel(it.level)]++;
    r.subs[it.sub] = (r.subs[it.sub] || 0) + 1;
  });
  var holes = res.filter(function(r){ return r.count === 0; }).map(function(r){ return r.u; });
  var thin  = res.filter(function(r){ return r.count > 0 && r.count < 5; }).map(function(r){ return r.u; });
  var total = 0; res.forEach(function(r){ total += r.count; });
  Object.keys(unmatched).forEach(function(k){ total += unmatched[k]; });
  return { units: res, holes: holes, thin: thin, unmatched: unmatched, total: total, covered: res.length - holes.length };
}
