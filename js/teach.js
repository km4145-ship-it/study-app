// ===== 純関数 =====
// 表記ゆれの正規化：全角→半角・カタカナ→ひらがな・小文字化・空白と記号を除去
function teachNormalize(s){
  return String(s == null ? '' : s)
    .replace(/[Ａ-Ｚａ-ｚ０-９]/g, function(c){ return String.fromCharCode(c.charCodeAt(0) - 0xFEE0); })
    .replace(/[ァ-ヶ]/g, function(c){ return String.fromCharCode(c.charCodeAt(0) - 0x60); })   // カタカナ→ひらがな
    .toLowerCase()
    .replace(/[\s、。．，,.!！?？「」『』()（）・〜ー-]/g, '');
}
// 説明文をルーブリックに照合：{hits:[w...], missing:[key...], score:0-100}
function teachScore(text, topic){
  var t = teachNormalize(text);
  var hits = [], missing = [];
  (topic.keys || []).forEach(function(k){
    var forms = [k.w].concat(k.alt || []);
    var hit = t.length > 0 && forms.some(function(f){ var nf = teachNormalize(f); return nf && t.indexOf(nf) >= 0; });
    if (hit) hits.push(k.w); else missing.push(k);
  });
  var total = (topic.keys || []).length || 1;
  var lenBonus = Math.min(20, Math.round(t.length / 25 * 20));   // 25文字以上でしっかり説明ボーナス満点
  var score = Math.round(80 * hits.length / total) + lenBonus;
  return { hits: hits, missing: missing, score: Math.max(0, Math.min(100, score)) };
}
// 教科からトピックを選ぶ（rnd は 0-1。省略時 Math.random）
function teachPickTopic(area, rnd){
  var pool = TEACH_TOPICS.filter(function(t){ return t.area === area; });
  if (!pool.length) pool = TEACH_TOPICS;
  var r = (typeof rnd === 'number') ? rnd : Math.random();
  return pool[Math.min(pool.length - 1, Math.floor(r * pool.length))];
}
